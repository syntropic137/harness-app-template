function installGitHooks(cwd: string): void {
  const globalHooksPath = git(['config', '--global', '--get', 'core.hooksPath'], { cwd, allowFailure: true });
  runInherit('pnpm', ['exec', 'lefthook', 'install', '--force'], cwd);
  git(['config', '--local', '--unset', 'core.hooksPath'], { cwd, allowFailure: true });

  if (!globalHooksPath) {
    return;
  }

  const wrapper = `#!/usr/bin/env bash
# Auto-generated by scripts/init.ts on \`just init\`.
# Chains the operator/global attribution hook configured via core.hooksPath,
# while keeping this repo on .git/hooks so lefthook also fires.
set -euo pipefail
GLOBAL_HOOK=${shellQuote(globalHooksPath)}/prepare-commit-msg
if [ -x "$GLOBAL_HOOK" ]; then
  "$GLOBAL_HOOK" "$@"
fi
`;
  const hookPath = join(cwd, '.git/hooks/prepare-commit-msg');
  writeText(hookPath, wrapper);
  chmodSync(hookPath, 0o755);
  console.log(`hooks: chained global prepare-commit-msg from ${globalHooksPath}; local .git/hooks remains active`);
}

/**
 * Resolve the canonical repo URL. Priority:
 *   1. `git remote get-url upstream` (the canonical sync remote, if the
 *      user already wired it up before `just init`).
 *   2. `git remote get-url origin` (the URL the consumer cloned/forked
 *      from — typically this IS the canonical repo for "Use this
 *      template" flows).
 *   3. Hard-coded fallback to `syntropic137/harness-app-template`.
 *
 * Returns the URL as-is; consumers can edit `.harness-provenance.json`
 * once if they need a different value (the file is immutable after init
 * per the `update.ts` guard).
 */
function resolveCanonicalRepo(cwd: string): string {
  const upstream = git(['remote', 'get-url', 'upstream'], { cwd, allowFailure: true });
  if (upstream) return upstream;
  const origin = git(['remote', 'get-url', 'origin'], { cwd, allowFailure: true });
  if (origin) return origin;
  return DEFAULT_CANONICAL_REPO;
}

/**
 * Resolve the canonical commit the consumer forked at. This is just
 * `HEAD` at init time — for "Use this template" and fork flows, HEAD
 * IS the canonical commit you started from. Returns 'unknown' if HEAD
 * doesn't yet exist (degenerate edge case, e.g. an empty repo).
 */
function resolveCanonicalCommit(cwd: string): string {
  return git(['rev-parse', 'HEAD'], { cwd, allowFailure: true }) || 'unknown';
}

export function writeHarnessProvenance(cwd: string, now = new Date()): HarnessProvenance {
  const manifest = JSON.parse(readText(join(cwd, 'harness.manifest.json'))) as HarnessManifest;
  const provenance: HarnessProvenance = {
    schemaVersion: '1.0',
    mode: 'fresh',
    template: manifest.name,
    templateVersion: manifest.version,
    standardVersion: manifest.standard,
    canonical_repo: resolveCanonicalRepo(cwd),
    canonical_commit: resolveCanonicalCommit(cwd),
    forked_at: now.toISOString(),
  };
  writeText(join(cwd, '.harness-provenance.json'), `${JSON.stringify(provenance, null, 2)}\n`);
  return provenance;
}

export function initProject(projectName: string, options: InitOptions = {}): string[] {
  const cwd = options.cwd ?? process.cwd();
  validateProjectName(projectName);

  if (isGitRepo(cwd) && !options.force) {
    const dirty = git(['status', '--porcelain'], { cwd });
    if (dirty) {
      throw new Error('working tree is not clean; rerun with --force to initialize anyway');
    }
  }

  const replacements: Array<[string, string]> = [
    ['@example/typescript', `@${projectName}/typescript`],
    ['@example/python', `@${projectName}/python`],
    ['@example/rust', `@${projectName}/rust`],
    ['example-typescript', `${projectName}-typescript`],
    ['example-python', `${projectName}-python`],
    ['example-rust', `${projectName}-rust`],
  ];
  const changed = replaceInTree(cwd, replacements);

  renameIfExists(join(cwd, 'ws_apps/example-typescript'), join(cwd, `ws_apps/${projectName}-typescript`));
  renameIfExists(join(cwd, 'ws_apps/example-python'), join(cwd, `ws_apps/${projectName}-python`));
  renameIfExists(join(cwd, 'ws_apps/example-rust'), join(cwd, `ws_apps/${projectName}-rust`));

  updateRootPyproject(cwd, projectName);
  updateHarnessComposeName(cwd, projectName);
  removeTemplateReadmeBlock(cwd);
  removeIfExists(join(cwd, 'TEMPLATE.md'));
  removeIfExists(join(cwd, '.github/ISSUE_TEMPLATE/template-question.md'));
  writeHarnessProvenance(cwd, options.now);

  /* v8 ignore next 7 */
  if (options.verify !== false) {
    runInherit('bun', ['--version'], cwd);
    runInherit('pnpm', ['install'], cwd);
    runInherit('cargo', ['check'], cwd);
    runInherit('uv', ['sync'], cwd);
    installGitHooks(cwd);
  }

  return changed;
}

export function parseCli(argv: string[]): { projectName: string; force: boolean; verify: boolean } {
  const force = argv.includes('--force');
  const noVerify = argv.includes('--no-verify');
  const projectName = argv.find((arg) => !arg.startsWith('--'));
  if (!projectName) {
    throw new Error('usage: bun run scripts/init.ts <project-name> [--force] [--no-verify]');
  }
  return { projectName, force, verify: !noVerify };
}

