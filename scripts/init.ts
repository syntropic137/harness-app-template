import { chmodSync, existsSync } from 'node:fs';
import { join } from 'node:path';
import { readText, removeIfExists, renameIfExists, replaceInTree, writeText } from './lib/fs';
import { git, isGitRepo, runInherit } from './lib/git';

interface HarnessManifest {
  name: string;
  version: string;
  standard: string;
}

/**
 * Git-native provenance written at `just init` time. Captures the
 * canonical-template repo + commit the consumer forked at, with NO
 * lab-side state — the lab is R&D only and not a live upstream. See
 * `docs/adrs/ADR-0015-cha-sync-source-of-truth.md`.
 */
interface HarnessProvenance {
  schemaVersion: '1.0';
  mode: 'fresh';
  template: string;
  templateVersion: string;
  standardVersion: string;
  canonical_repo: string;
  canonical_commit: string;
  forked_at: string;
}

const DEFAULT_CANONICAL_REPO = 'https://github.com/syntropic137/harness-app-template';

export interface InitOptions {
  cwd?: string;
  force?: boolean;
  now?: Date;
  verify?: boolean;
}

const PROJECT_RE = /^[a-z0-9][a-z0-9-]{0,213}$/;

export function validateProjectName(projectName: string): void {
  if (!PROJECT_RE.test(projectName)) {
    throw new Error('project name must match npm-safe pattern ^[a-z0-9][a-z0-9-]*$ and be <= 214 chars');
  }
}

function removeTemplateReadmeBlock(cwd: string): void {
  const path = join(cwd, 'README.md');
  if (!existsSync(path)) return;
  const original = readText(path);
  const updated = original.replace(/\n?<!-- TEMPLATE-DOC-START -->[\s\S]*?<!-- TEMPLATE-DOC-END -->\n?/m, '\n');
  if (updated !== original) {
    writeText(path, updated);
  }
}

function updateRootPyproject(cwd: string, projectName: string): void {
  const path = join(cwd, 'pyproject.toml');
  if (!existsSync(path)) return;
  const original = readText(path);
  // The root pyproject declares the virtual uv workspace; its name must
  // NOT collide with the inner Python member's name (otherwise
  // `uv sync` refuses with "two workspace members are both named X").
  // Suffix the root with `-monorepo` to keep it distinct from the
  // `${projectName}-python` member that `replaceInTree` rewrites inside
  // `ws_apps/${projectName}-python/pyproject.toml`.
  const updated = original.replace(/^name = "agentic-harness-monorepo"$/m, `name = "${projectName}-monorepo"`);
  if (updated !== original) {
    writeText(path, updated);
  }
}

function updateHarnessComposeName(cwd: string, projectName: string): void {
  const path = join(cwd, 'harness/observability/compose.harness.yml');
  if (!existsSync(path)) return;
  const original = readText(path);
  const updated = original.replace(/^name:\s*agentic-harness-monorepo\s*$/m, `name: ${projectName}`);
  if (updated !== original) {
    writeText(path, updated);
  }
}

